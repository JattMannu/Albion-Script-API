namespace Ennui.Api.Meta
{
	/// <summary>
	/// Represents each type of resource within the game world.
	/// </summary>
    public enum ResourceType
    {
		/// <summary>
		/// Represents a resource that gives rock items.
		/// </summary>
        Rock,
		/// <summary>
		/// Represents a resource that gives wood items.
		/// </summary>
		Wood,
		/// <summary>
		/// Represents a resource that gives ore items.
		/// </summary>
		Ore,
		/// <summary>
		/// Represents a resource that gives hide items.
		/// </summary>
		Hide,
		/// <summary>
		/// Represents a resource that gives fiber items.
		/// </summary>
		Fiber,
		/// <summary>
		/// Represents a resource that gives silver coins.
		/// </summary>
		Coins,
		/// <summary>
		/// Represents an unknown resource.
		/// </summary>
		Unknown
    }
}
