using System;

namespace Ennui.Api.Meta
{
	/// <summary>
	/// Represents the possible movement states that an entity can have.
	/// </summary>
	public enum MovementState
	{
		/// <summary>
		/// Represents an entity that is not moving.
		/// </summary>
		Idle = 0,
		/// <summary>
		/// Represents an entity that is moving normally.
		/// </summary>
		Moving = 1,
	}
}

